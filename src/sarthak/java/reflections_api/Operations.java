package reflections_api;

public class Operations {
	
	public void add(int a, int b) {
		int res = a+b;
		System.out.println(res);
	}
	
	public void diff(int a, int b) {
		int res = a-b;
		System.out.println(res);
	}
	
	
	public void mul(int a, int b) {
		int res = a*b;
		System.out.println(res);
	}
	
	
	public void div(int a, int b) {
		int res = a/b;
		System.out.println(res);
	}

}
